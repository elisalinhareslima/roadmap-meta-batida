const navItems = document.querySelectorAll(".nav-item");
const views = document.querySelectorAll(".view");
const lessonList = document.getElementById("lessonList");
const lessonDetail = document.getElementById("lessonDetail");
const progressLabel = document.getElementById("progressLabel");
const progressFill = document.getElementById("progressFill");
const completed = new Set(JSON.parse(localStorage.getItem("roadmapCompleted") || "[]"));
let selectedLesson = 0;

function saveProgress() {
  localStorage.setItem("roadmapCompleted", JSON.stringify([...completed]));
  const pct = Math.round((completed.size / lessons.length) * 100);
  progressLabel.textContent = `${pct}%`;
  progressFill.style.width = `${pct}%`;
}

function setView(viewId) {
  navItems.forEach((item) => item.classList.toggle("active", item.dataset.view === viewId));
  views.forEach((view) => view.classList.toggle("active", view.id === viewId));
}

function renderLessonList() {
  const phases = [...new Set(lessons.map((lesson) => lesson.phase))];
  lessonList.innerHTML = phases
    .map((phase) => {
      const phaseLessons = lessons
        .map((lesson, index) => ({ ...lesson, index }))
        .filter((lesson) => lesson.phase === phase)
        .map((lesson) => {
          const checked = completed.has(lesson.index) ? "checked" : "";
          const active = lesson.index === selectedLesson ? " active" : "";
          return `
            <button class="lesson-button${active}" data-lesson="${lesson.index}">
              <input type="checkbox" data-check="${lesson.index}" ${checked} aria-label="Marcar aula como estudada" />
              <strong>${lesson.title}</strong>
              <small>${lesson.duration}</small>
            </button>
          `;
        })
        .join("");
      return `<div class="phase-title">${phase}</div>${phaseLessons}`;
    })
    .join("");
}

function renderLessonDetail() {
  const lesson = lessons[selectedLesson];
  lessonDetail.innerHTML = `
    <div class="lesson-meta">
      <span class="pill">${lesson.phase}</span>
      <span class="pill alt">${lesson.duration}</span>
    </div>
    <h2>${lesson.title}</h2>
    <p>${lesson.summary}</p>
    <div class="detail-grid">
      <section class="detail-box">
        <h3>Objetivo de estudo</h3>
        <p>${lesson.objective}</p>
      </section>
      <section class="detail-box">
        <h3>Pontos principais</h3>
        <ul>${lesson.keyPoints.map((item) => `<li>${item}</li>`).join("")}</ul>
      </section>
      <section class="detail-box">
        <h3>Exercicio pratico</h3>
        <ul>${lesson.practice.map((item) => `<li>${item}</li>`).join("")}</ul>
      </section>
      <section class="detail-box">
        <h3>Como registrar</h3>
        <p>Anote uma frase-chave da aula, uma pergunta que voce vai usar e uma mudanca concreta para testar em uma conversa real.</p>
      </section>
    </div>
    <a class="open-lesson" href="${lesson.url}" target="_blank" rel="noreferrer">Abrir este conteudo na Hubla</a>
  `;
}

navItems.forEach((item) => {
  item.addEventListener("click", () => setView(item.dataset.view));
});

lessonList.addEventListener("click", (event) => {
  const checkbox = event.target.closest("[data-check]");
  if (checkbox) {
    const index = Number(checkbox.dataset.check);
    if (checkbox.checked) completed.add(index);
    else completed.delete(index);
    saveProgress();
    return;
  }

  const button = event.target.closest("[data-lesson]");
  if (!button) return;
  selectedLesson = Number(button.dataset.lesson);
  renderLessonList();
  renderLessonDetail();
});

renderLessonList();
renderLessonDetail();
saveProgress();
